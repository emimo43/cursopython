# En esta clase veremos los operadores de comparacion
igual_que = 5 == 6 # igual que
distinto_de = 5 != 6 # distinto de
mayor_que = 5 > 6 # mayor que
menor_que = 5 < 6  # menor que
mayor_o_igual = 5 >= 6 # mayor o igual
menor_o_igual = 5 <= 6 # menor o igual

# Ahora mostramos por consola
print(igual_que)
print(distinto_de)
print(mayor_que)
print(menor_que)
print(mayor_o_igual)
print(menor_o_igual)

# Calculos combinados
a = 5
