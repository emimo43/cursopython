# En esta clase veremos los tipos de datos en Python
# Tipo de dato entero o largo
numero = 15
#str(numero) # Aqui convierto el entero en string
print(type(numero)) # En esta manera solo me muestra el tipo de dato
print(numero, type(numero)) # Esta es otra forma de mostrar el tipo de dato, asi me muestra el valor y el tipo de dato

# Tipo de dato floatante(numeros reales)
numero_flotante = 15.5
print(numero_flotante, type(numero_flotante))

# Tipo de dato numero complejo (complex)
numero_complejo = 5 + 6j
print(numero_complejo, type(numero_complejo))

# Tipo de dato String
nombre = "Enrique"
print(nombre, type(nombre))

# Tipo de dato Booleano
verdadero_Falso = 3 == 3
print(verdadero_Falso, type(verdadero_Falso))