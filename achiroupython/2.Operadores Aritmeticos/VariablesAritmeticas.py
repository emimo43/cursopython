# Variables Aritmeticas
num1 = 10
num2 = 5

print(num1)
print(num2)

print(num1 + num2) # Operacion de Suma con Variables
print(num1 - num2) # Resta
print(num1 * num2) # Multiplicacion
print(num1 / num2) # Division
print(num1 // num2) # Division Entera
print(num1 ** num2) # Exponenciacion
print(num1 % num2) # Modulo -> Residuo de la division

suma = num1 + num2 # Suma contendra la operacion de suma
print(suma)

resta = num1 - num2
print(resta)