cadena = "Este es un ejemplo de Substring (Debanado de Cadenas)"

print(len(cadena)) # El metodo len nos indica la cantidad de caracteres de la cadena

print(cadena[2]) # Con este metodo me mostrara el caracter en la posicion2 que seria la t

print(cadena[50])

print(cadena[0])

print(cadena[7]) # Aqui seria un espacio vacio

print(cadena[0 : 10]) # Le estoy indicando a Python que me muestre desde el caracter 0 hasta el 10 que sera el 9 por que 0 es el primer caracter

print(cadena[0 : 9])

print(cadena[0 : 11])

print(cadena[ : 20]) # Aqui comienzo desde el 0 hasta el 20 que seria el 19

print(cadena[20 : ]) # Aqui comenzara desde la posicion 20 hasta el final

print(cadena[-1]) # Comienza desde la inversa la cadena

print(cadena[-3])