cadena = "Estoy utilizando los metodos de Python"
cadena2 = "ESTOY UTILIZANDO LOS METODOS DE PYTHON"
cadena3 = "estoy utilizando los metodos de Python"

print(cadena2.lower()) # El metodo lower() dejara en minuscula la cadena
print(cadena.upper()) # El metodo upper() convierte todo a mayuscula
print(cadena3.capitalize()) # El metodo capitalize() Muestra solo la primera letra en mayuscula
print(cadena.title()) # Muestra todas las inciales de los String en Mayuscula
print(cadena.swapcase()) # Este metodos convierte las mayusculas a minusculas y viciversa