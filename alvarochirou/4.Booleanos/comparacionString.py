# Comparacion de String usando el metodo len

cadena1 = "Comparaciones de Strings"
cadena2 = "Comparacion de Strings"
cadena3 = "Comparación de Strings"
 
print(cadena1 > cadena2)
print(cadena1 > cadena3)
 
print(len(cadena1) > len(cadena2))
print(len(cadena1) > len(cadena3))  

print(len(cadena1))
print(len(cadena2))
print(len(cadena3))