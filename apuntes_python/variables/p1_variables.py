# En esta clase veremos las variables en Python

# Variables numericas
numeroEntero = 10
numeroReal = 5.5

# Ahora con la funcion type() nos mostrara por consola el tipo de dato
print(type(numeroEntero))
print(type(numeroReal))

# Ahora mostramos por consola solo el resultado de la variable
print(numeroEntero)
print(numeroReal)

# Variables de texto
nombre = 'Enrique'
apellido = "Lueiza"

print(type(nombre))
print(type(apellido))

print(nombre)
print(apellido)

# Variables Booleandas
cierto = True # Tambien puede ser 1
falso = False # Tambien puede ser 0

print(type(cierto))
print(type(False))

print(cierto)
print(falso)