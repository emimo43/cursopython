"""Quitar espacios en blanco de una frase"""

# Esto lo realizaremos con la funcion strip()

frase = " Hola, Develoteca " # Aqui generamos un espacio en blanco antes de la H
# Vamos a quitar los espacios al principio y al final de una frase
print(frase.strip()) # El metodo strip eliminara los espacios en blanco