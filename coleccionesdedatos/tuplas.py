# Tuplas es una coleccion de elementos ordenada que no se puede modificar
tupla_colores = ("rojo", "verde", "amarillo")
print(tupla_colores)

# Podemos mostrar las tuplas con un ciclo for
for color in tupla_colores:
    print(color)

# Para ver un dato de la tupla lo hacemos de la siguiente forma
print(tupla_colores[2]) # Veremos que lo que mostraremos es el color amarillo    

# Para saber la longitud de la tupla usamos el metodo len
print(len(tupla_colores))