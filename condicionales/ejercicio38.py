# Uso del ciclo While.

# Declaramos la variable
numero = 1 # La variable se inicia en 1

# Ciclo
while numero < 6: # Mientras numero es menor que 6, esta condicion se ejecuta
    print(numero)
    numero += 1 # Si la condicion se cumple, la variable numero se incrementa en 1

# Tambien podemos hacer operaciones con el ciclo while
numero2 = 1

# Ciclo
while numero2 < 6:
    print(numero2 * 10) # Aqui la variable numero la multiplicamos por 10
    numero2 += 1 # Se incrementa la variable