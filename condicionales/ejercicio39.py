#Uso del ciclo while y el if

numero = 1

# Imprimir los numeros pares menores a 10
while numero < 10: # Mientras numero es menor a 10
    if(numero % 2 == 0): # Si el modulo de la division del numero con dos es igual a 0
        print(numero) # Mostrara el numero
    numero += 1 # EL numero se incrementa en uno    


numero2 = 1

# Imprimir los numeros pares menores o igual 10
while numero2 <= 10: # Mientras numero es menor o igual 10
    if(numero2 % 2 == 0): # Si el modulo de la division del numero con dos es igual a 0
        print(numero2) # Mostrara el numero
