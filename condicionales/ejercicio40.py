# Usando el Ciclo While con el else

numero = 1
while numero < 10: # Mientras numero es menor a 10
    print(numero) # Mostramos en consola el valor de la variable
    numero += 1 # La variable se incrementa en 1

else:
    print("Se acabo") # Cuando el ciclo while ya no se cumpla se mostrara el mensaje del else   