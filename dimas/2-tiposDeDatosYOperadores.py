# Variables
# Tipos de datos numericos

numero_uno = 1 # Esta es una variable con asignacion

print(numero_uno) # Imprimimos la variable
print("numero_uno") # Esto no es una variable es una cadena de texto

numero_entero = 1 # Esta es una variable de tipo entero.
print(numero_entero)

numero_decimal = 2.46 # Esta es una variable de tipo Float
print(numero_decimal)

numero_entero = numero_entero + 2 # Aqui estamos sumando a la variable de numero_entero + 2
print(numero_entero)

numero = numero_entero + numero_decimal # Aqui estamos creando una nueva variable a la cual sumamos numero entero + numero decimal y almacenamos el valor en la variable numero
print(numero) # Aqui imprimimos el valor de numero

numero_entero += 2 # Esta operacion lo que nos permite es sumarle 2 directo a la variable numero_entero
print(numero_entero)

variable_numerica = 20
variable_numerica = variable_numerica - 10
print(variable_numerica)

variable = 10
variable = variable / variable_numerica
print(variable)

numero = 2/2+3
print(numero)

multiplicacion = 2*3
print(multiplicacion)

multiplicacion = numero_entero * numero_decimal
print(multiplicacion)

potencia = 2**3 # Esto es 2 elavado a 3
modulo = 11%2 # Esto es el residuo de la division
print(potencia)
print(modulo)

