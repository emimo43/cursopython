# Operadores matematicos

# modulo

# Declaramos dos variables numericas
numero1 = 10 # Dividendo
numero2 = 3 # Divisor

# Calculamos el coeciente y el resto de la division
cociente = numero1 / numero2
resto = numero1 % numero2

# Imprimimos el coeciente y el resto
print(f"Cociente: ",cociente)
print(f"Resto: ",resto)

# Division entera //

# Declaramos dos variables numericas
numero1 = 10 # Dividendo
numero2 = 3 # Divisor

# Calculamos el coeciente y el resto de la division
cociente = numero1 // numero2
resto = numero1 % numero2

# Imprimimos el coeciente y el resto
print(f"Cociente: ",cociente)
print(f"Resto: ",resto)

