# En esta clase veremos los operadores de asignacion, los cuales asignan los valores a las variables

miVariable = 10 # En este caso vemos que la variable tiene un valor de 10
print(miVariable)

# Reasignaremos el valor a la variable
miVariable = miVariable + 1 # Aqui le estamos sumando el valor de 1 a la variable
print(miVariable)

# Una forma mas resumida resumida para reasignar es con el incremento y el decremento
# Incremento
miVariable += 1
print(miVariable)

# Decremento
miVariable -= 2 # En ese caso estamos restando 2 a miVariable
print(miVariable)

# Multiplicacion
miVariable *= 3 # Multiplicamos por 3
print(miVariable)

# Division
miVariable /= 2 # Division por 2
print(miVariable)

# Division entera
miVariable //= 2 # Hacemos la division entera
print(miVariable)

# Modulo
miVariable %= 2 # Realizamos el modulo
print(miVariable)