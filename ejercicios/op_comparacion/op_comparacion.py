# En esta clase veremos los operadores de comparacion en Python

# Creamos dos variables y las inicializamos con sus correspondientes valores
a = 4
b = 2

# Creamos una variable resultado en las cuales almacenaremos los valores correspondientes

# Operador de igualdad ==
resultado = a == b # Aqui comparamos si a es igual a b, lo cual es falso o sea el resultado es True
print(f"El resultado es: {resultado}")

# Operador distinto de !=
resultado = a != b # En este caso el resultado en la consola debe ser True, debido a que a es distinto de b
print(f"El resultado de la operacion es: {resultado}")

# Operador > Mayor que
resultado = a > b # En esta operacion el resultado es True, debido a que a contiene un valor mayor que b
print(f"El resultado de la operacion es: {resultado}")

# Operador >= Mayor o igual
resultado = a >= b # En este caso, el resultado es True, debido a que a es mayor que b
print(F"El resultado es: {resultado}")

# Operador < Menor que
resultado = a < b # En este caso la operacion tendra el resultado de false, debido a que a no es menor que la variable b
print(f"El resultado de la operacion es: {resultado}")

# Operador <= Menor o igual
resultado = a <= b # En este caso el resultado en consola sera false, debido a que ninguna de las condiciones se cumple
print(f"El resultado es: {resultado}")