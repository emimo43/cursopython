# En este ejercicio haremos una suma con variables en Python

# Sumar dos numeros y mostrar su resultado

# Solicitamos los datos al Usuario
numero1 = int(input("Favor ingresa el primer numero entero: "))
numero2 = int(input("Favor ingresa el segundo numero entero: "))

# Ahora creamos la variable suma, la cual contendra la operacion y el resultado de esta
suma = numero1 + numero2

# Ahora con la funcion print, mostramos el resultado por consola
print(f"El resultado de la suma de ambos numeros es: {suma}")