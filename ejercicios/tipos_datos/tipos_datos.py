# Tipo de dato str (String -> Cadena de caracteres, este tipo de dato usa comillas dobbles o simples)
saludo = "Hola alumnos"
# Una manera de saber el tipo de dato es usando la funcion type
print(type(saludo))
print(saludo)

# Tipo de dato int (entero -> numeros positivos o negativos)
edad = 32
print(type(edad))
print(edad)

x = 10
print(x)
print(type(x)) # Con la funcion type() podemos determinar de que tipo de dato es la variable

