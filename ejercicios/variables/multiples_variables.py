# Multiples variables en Python

nombre = "Enrique Lueiza" # Esta variable almacena mi nombre
print(nombre)

# Podemos agregar multiples variables en una linea, pero no es una practica no muy recomendable
nombre, apellido = "Enrique", "Lueiza"

# Ahora mostramos por consola 
print(nombre)
print(apellido)