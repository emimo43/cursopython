# Ejercicio sobre variables en Python

# Variables numericas
numero_entero = 10
numeroEntero = 11
numero_real = 5.5

# Ahora mostramos por consola y vemos el resultado y tipo de dato con la funcion type()
print(numero_entero)
print(type(numero_entero))
print("-----")
print(numeroEntero)
print(type(numeroEntero))
print("----------")
print(numero_real)
print(type(numero_real))
print("------------")

# Variables de texto
nombre = "Enrique"
apellido = "Lueiza"

print(type(nombre))
print(nombre)
print("----------------")
print(type(apellido))
print(apellido)
print("----------------------------")

# Variables booleanas
cierto = True
falso = False
print(type(cierto))
print(cierto)
print("-----------------")
print(type(falso))
print(falso)
print("------------")