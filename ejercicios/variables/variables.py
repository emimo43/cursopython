# En esta clase veremos las variables en Python

edad = 20 # Esta es una variable de tipo int
mensaje = "Bienvenido" # Esta es una variable de tipo String -> str
nombre = "Enrique Lueiza" # Esta es otra variable tipo str

# Con la funcion type() podemos saber de que tipo de dato es una variable
print(type(edad))
print(type(mensaje))
print(type(nombre))

# Ahora mostramos por consola
print(edad)
print(mensaje)
print(nombre)

# Ahora veremos la reasignacion de variables
numero = 89
print(numero)
# Ahora cambiaremos el valor de la variable
numero = 500
print(type(numero))
print(numero)

# Ahora veremos las variables de tipo flotante (float)
flotante = 3.14
print(type(float))
print(flotante)