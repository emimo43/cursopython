# Operadores de comparacion

x = 3
y = 5

# Comparacion igual
print(x==y) # Estamos analizando si x es igual a y == es un operador de comparacion

# Comparacion diferente (no igual)
print (x!=y) # Si x es diferente a y

# Mayor y menor
print(x > y)
print(x < y)

# Mayor o igual - menor o igual
print(x >= y)
print(x <= y)