'''Sumar dos numeros y mostrar su resultado'''
# Declaramos las variables y le solicitamos los datos al usuario
numero1 = int(input("Favor ingrese el primer numero: " ))
numero2 = int(input("Favor ingrese el segundo numero: "))

# Ahora creamos una variable suma, la cual almacenara el resultado de la adicion
suma = numero1 + numero2

# Mostramos por consola el resultado
print(f"La suma de los dos numeros es: {suma}")