'''
    Sumar dos numeros y mostrar su resultado
'''

# Solicitamos los datos al Usuario
numero1 = int(input("Favor ingresar el primer numero entero: "))
numero2 = int(input("Favor ingresar el segundo numero entero: "))

# Ahora creamos la variable suma, la cual contendra la operacion y resultado de este
suma = numero1 + numero2

# Ahora con la funcion print, mostramos el resultado de esta
print(f"El resultado de la suma de ambos numeros es: {suma}")