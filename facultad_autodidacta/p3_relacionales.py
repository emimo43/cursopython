# En este capitulo veremos los operadores relacionales en Python

# Comparacion

comparacion = 5 == 5 # Aqui consultamos si 5 es igual a 5
print(comparacion)
print(type(comparacion))
print(" ")

# Diferente
diferente = 5 != 5 # Consultamos si 5 es diferente a 5
print(diferente)
print(type(diferente))
print(" ")

# Mayor que
mayorQue = 5 > 5 # Consultamos si 5 es mayor que 5
print(mayorQue)
print(type(mayorQue))
print(" ")

# Menor que
menorQue = 5 < 5 # Consultamos si 5 es menor que 5
print(menorQue)
print(type(menorQue))
print(" ")

# Mayor o igual que
mayorIgualQue = 5 >= 5 # Consultamos si 5 es mayor o igual  que 5
print(mayorIgualQue)
print(type(mayorIgualQue))
print(" ")

# Menor o igual que
menorIgualQue = 5 <= 5 # Consultamos si 5 es menor que 5
print(menorIgualQue)
print(type(menorIgualQue))
print(" ")
