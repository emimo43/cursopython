# En esta clase veremos los operadores logicos de Python

# And logico
logicoAnd = 5 and 5 # 5 y 5 --> Los dos deben ser iguales
print(logicoAnd)
print(type(logicoAnd))
print(" ")

# Or logico
logicoOr = 5 or 5 # Para que sea verdadera, basta una sola condicion
print(logicoOr)

# not logico --> niega la condicion
logicoNot = not True
print(logicoNot)