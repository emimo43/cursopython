# En esta clase comenzaremos a ver la coleccion de datos -> Diccionarios

# Generamos un diccionario -> clave -> valor

diccionario1 ={
    "clave" : 1,
    "clave2" : 2,
    "clave3" :'hola',
    "nombre" : "facultad"
    
}

# Ahora mostramos por consola el diccionario
print(diccionario1)
print(" ")

# Ahora veremos como imprimir un elemento del diccionario
print((diccionario1["nombre"])) # En este caso nos debe imprimir facultad
print(" ")

# Ahora veremos como modificar un elemento del diccionario
diccionario1["clave1"] = "Autodicacta"
print(diccionario1["clave1"])
print(" ")

# Ahora veremos el diccionario modificado
print(diccionario1)
