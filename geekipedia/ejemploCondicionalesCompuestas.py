# Ejemplo de condicionales compuestas (if - else)

# Declaramos una variable
numero_uno = 5

# Generamos la condicon if
if numero_uno == 5: # Si numero uno es igual a 5 se ejecutara la instruccion if
    print("El numero es cinco")
else:
    print("El numero NO es cinco") # Si la concicion anterior no es verdadera saltara a la condicion else y ejecuta esta instruccion
print("Fin.")    
