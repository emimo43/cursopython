nombre = input("¿Cual es tu nombre?: ")
print(f"Hola {nombre}, vamos a realizar una suma.")

# Realizamos la solicitud de datos al Usuario
numero_uno = int(input("Por favor introduce el primer valor: "))
numero_dos = int(input("Por favor introduce el segundo valor: "))

# Ahora realizamos la operadion de la suma
resultado = numero_uno + numero_dos

# Ahora mostramos por consola el resultado
print(f"{nombre}, el resultado de la suma es: {resultado}")
                 
