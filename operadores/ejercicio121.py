# En esta clase veremos los operadores aritmeticos en Python
# Probando las operaciones aritmeticas
# Solicitamos los datos al usuario

print("Deme un valor 1")
a = int(input()) # Aqui casteamos el valor de string a entero

print("Deme un valor 2")
b = int(input())

# EJemplo Suma
suma = a + b

# Ejemplo Resta
resta = a - b

# Ejemplo Multiplicacion
multiplicacion = a * b

# Ejemplo Dividir
division = a / b

# Division entera (floor)
floor = a //b

# Exponenciacion
exponente = a ** b

# Modulo
modulo = a % b # Residulo de la division

# Mostramos por consola
print(suma)
print(resta)
print(multiplicacion)
print(division)
print(floor)
print(exponente)
print(modulo)