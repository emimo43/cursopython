# Operadores de Asignacion
x += 4 # Es lo mismo que indicar (x = x + 4)
print(x) # Sera 9

x -= 4 # Es lo mismo que indicar ( x = x -4)
print(x) # Sera 5

x *= 4 # Es lo mismo que indicar (x = x * 4)
print(x) # Sera 20

x /= 4 # Es lo mismo que indicar (x = x / 4)
print(x) # Sera 5.0

x %= 4 # Es lo mismo que indicar (x = x % 4)
print(x) # Sera 1.0

x **= 4 # Es lo mismo que indicar (x = x ** 4)
print(x)

x //= 4 # Es lo mismo que indicar (x = x // 4)
print(x)