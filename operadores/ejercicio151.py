# Operadores Logicos

# Variables de tipo boleando (Verdad o Falso)
a = True
b = False

# Operadores

# and &
print ( a and b) # and es Y, quiere decir solo seria verdadero si ambas variables son verdadero, tambien se puede usar el & (es lo mismo que and)
print(a & b)

# or | -> Solo sera verdadero si ambos son falsos
print(a or b)
print(a | b)

# not (negacion)
print(not a)