# Operadores de asignacion
numero = 5
print(numero)

numero = numero +4
print(numero)

numero = 5
print(numero)

# numero = numero + 4 es lo mismo que
numero += 4
print(numero)

numero = numero - 3
print(numero)

numero -= 3
print(numero)

numero = numero * 4
print(numero)

numero *= 4
print(numero)

numero /= 4
print(numero)

numero = 2
numero = numero ** 3
print(numero)

numero = 2
numero **= 3
print(numero)