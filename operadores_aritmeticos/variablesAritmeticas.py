# En esta clase veremos las variables aritmeticas en Python, estan guardan valores numericos

# Creamos dos variables
num1 = 10
num2 = 5

# Ahora la vamos a mostrar por consola estos valores
print(num1)
print(num2)

# Ahora vamos a generar las diferentes operaciones aritmeticas con variables

# suma
suma = num1 + num2
print(suma)

# resta
resta = num1 - num2
print(resta)

# multiplicacion
multiplicacion = num1 * num2
print(multiplicacion)

# Division
division = num1 / num2
print(division)

# Division entera
division_entera = num1 // num2
print(division_entera)

# Exponenciacion
exponente = num2 ** num2
print(exponente)

# Modulo
modulo = num1 % num2
print(modulo)
