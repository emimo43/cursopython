# Listas en Python

miLista = ["Maria", "Pepe", "Marta", "Antonio"]

print(miLista[:]) # Aqui vamos a imprimir toda la lista
#Para acceder a un elemento en concreto lo hacemos de la siguiente manera
print(miLista[2]) # Indicamos la posicion que necesitamos ver
# Si tratamos de acceder a una posicion que no existe, Python nos dara una excepcio "Error"
# Para acceder de derecha a izquierda se usa el signo negativo, cuenta desde -1
print(miLista[-2])

