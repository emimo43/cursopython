# En esta clase veremos la funcion pre-definida input() -> Ingreso de datos por teclado

# Declaramos la variable en la cual almacenaremos los datos ingresados
nombre = input("Favor, introduce tu nombre: \n") # -> Con la sentencia \n doy un salto de linea para ingresar los datos

# Ahora mostramos por consola el resultado ayudados de la funcion print
print(f"El nombre almacenado es: {nombre} .")