# Clase en la cual veremos operadores aritmeticos en Python
# Declaramos dos variables numericas
numero1 = 100
numero2 = 500

# Calculamos el resultado de la suma de los dos valores
resultado = numero1 + numero2

# Imprimimos el resultado en consola
print(resultado)
print(f"El resultado de la suma es: {resultado}")

# Declaramos dos variables numericas
numero1 = 10 # Dividendo
numero2 = 3 # Divisor

# Calculamos el cociente y el resto de la division
cociente = numero1 / numero2
cociente2 = numero1 // numero2 # Division entera
resto = numero1 % numero2

# Imprimimos el cociente y modulo
print(f"El cociente: {cociente}")
print(f"El cociente de la division entera es: {cociente2}")
print(f"El resto de la division es: {resto}")

# Potencia (Exponenciacion)
operacion = 2 ** 10 # Es 2 elevado a 10
print(f"El resultado de 2 elevado a 10 es: {operacion}")