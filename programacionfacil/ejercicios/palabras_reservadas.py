'''
    En Python existen 35 palabras reservadas, como acceder a ellas es con la funcion import keyword y luego mostrar por consola
'''
import keyword # Importamos todo con esta funcion en lo que incluye las palabras reservadas de Python
print(keyword.kwlist) # El metodo kwlist nos permite obtener la informacion de las palabras reservadas de Python, nos muestra la lista de palabras reservadas de Python