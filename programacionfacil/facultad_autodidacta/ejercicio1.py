'''
    Ejercicio1 -> Sumar dos numeros y mostrar su resultado
'''

# Solicitamos los datos al Usuario
numero1 = int(input("Favor ingresar el primer numero: "))
numero2 = int(input("Favor ingresar el segundo numero: "))

# Ahora creamos una variable suma, la cual contendra la operacion solicitada
suma = numero1 + numero2

# Ahora creamos una variable llamada resultado, la cual almacenara el resultado que contiene la variable suma
resultado = suma

# Ahora mostramos por consola el resultado de la suma
print(f"El resultado de la suma de ambos numeros es: {suma}")