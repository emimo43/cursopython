# En esta clase veremos las variables en Python

# Variables numericas

'''
    Resumen
    con
    varias lineas
    
'''
numero_entero = 10
numeroEntero = 10 # Este tipo de escritura es camelCase
numeroReal = 5.5

# Ahora mostramos por consola y vemos el tipo de dato con la funcion type
print(numero_entero)
print(type(numero_entero))
print(" ")
print(numeroEntero)
print(type(numeroEntero))
print(" ")
