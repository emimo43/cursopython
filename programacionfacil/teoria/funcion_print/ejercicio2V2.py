'''
    Ejercicio 2 -> Hallar la superficie de un cuadrado conociendo el valor de un lado:
    Formula -> superficie = lado * lado
'''

# Solicitamos los datos al Usuario
lado = int(input("Favor ingresar el valor del lado: "))

# Ahora creamos la variable superficie, donde almacenaremos el resultado
superficie = lado * lado

# Ahora mostramos por consola el resultado
print(f"La superficie del cuadrado es: {superficie}")
print(" ")
print("Fin del Programa")
