'''
    En esta clase veremos tipos de datos enteros
    
'''

# type(entero) -> devolvera int
entero = 23
print(entero)
print(type(entero))

# Para almacenar un numero tipo Long le agregamos una L al final
# type(entero) devolvera un long
entero2 = 23
print(entero2)
print(type(entero2))

# Complejos
complejo = 2.1 + 7.8j
print(complejo)
print(type(complejo))
