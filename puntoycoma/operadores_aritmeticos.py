# suma +

suma = 2 + 2
print(f"La suma de 2 + 2 es igual a: {suma}")

# resta -

resta = 10 - 5
print(f"El resultado de la resta de 10 - 5 es {resta}")

# multiplicacion *

multiplicacion = 2 * 2
print(f"El resultado de la multiplicacion de 2 * 2 es: {multiplicacion}")

# division /
division = 9 / 3
print(f"El resultado de la division entre 9 / 3 es: {division}")

# division entera //
division_entera = 9 // 3
print(f"El resultado de la division entre 9 // 3 es: {division_entera}")

# modulo %
modulo = 10 % 5
print(f"El resultado del modulo de 10 % 5: {modulo}")

# potencia **
potencia = 2 ** 3
print(f"El resultado de la potencia de 2 ** 3 es: {potencia}")

