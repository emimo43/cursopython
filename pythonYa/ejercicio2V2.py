'''
Problema: Hallar la superficie de un cuadrado conociendo el valor de un lado.
la formula es:
superficie = lado * lado
'''
# Solicitamos los datos al Usuario
lado = int(input("Favor ingresar el valor del lado del cuadrado: "))

# Ahora calculamos la superficie con la formula
superficie = lado * lado

# Ahora mostramos por consola el resultado de la operacion
print(f"La superficie del cuadrado es: {superficie}")