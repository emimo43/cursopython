"""Problema:
Hallar la superficie de un cuarado conociendo el valor de un lado
La formula es superficie = lado * lado"""

# Solicitamos los datos al usuario
lado = int(input("Favor ingrese el valor de un lado: "))

# Ahora creamos la variable superficie en el cual guardaremos el resultado de la operacion

superficie = lado * lado

# Ahora mostramos por consola el resultado
print(f"La superficie del cuadrado es: {superficie}")