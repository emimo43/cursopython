'''
    Ejercicio 2.-
    Problema -> Hallar la superficie de un cuadrado conociendo el valor de un lado
    
    Formula -> superficie = lado * lado
'''
# Solicitamos los datos al Usuario
lado = int(input("Favor ingrese el valor del lado del cuadrado: "))

superficie = lado * lado # Esta es la formula

# Ahora mostramos por consola el resultado
print(f"La superficie del cuadrado es: {superficie}")