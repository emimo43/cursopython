"""Problema: Realizar la carga del lado de un cuadrado, mostrar por pantalla el perimetro del mismo(El perimetro de un cuadrado se calcula multiplicando el valor del lado por cuatro)"""

# Solicitamos los datos al Usuario
lado = int(input("Favor ingresar el valor del lado: "))

# Calculamos la operacion para obtener el perimetro
perimetro = lado * 4

# Mostramos por consola la operacion
print(f"El valor del perimetro es: {perimetro}")
