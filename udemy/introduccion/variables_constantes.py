# En esta clase veremos las variables y constantes en Python

# Variables
cantidad_personas = 100
print(cantidad_personas)
# Modificamos la cantidad en la variable
cantidad_personas = 150
print(cantidad_personas)
# Las variables como su nombre lo dicen varian
cantidad_personas = 170
print(cantidad_personas)

# Constantes -> En Python no existen, pero se crea una variable en mayuscula y se toma como Constante
PI = 3.14 # Aqui lo tomamos como constante
print(PI) # Se puede modificar, pero los desarrolladores de Python las mayusculas en las variables la toman como constante