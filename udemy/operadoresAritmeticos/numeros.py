# En esta clase veremos los numeros en Python, estos son enteros y flotantes
num1 = 40 # Tipo entero
num2 = 99.99 # Tipo Flotante

print(type(num1))
print(type(num2))

print(num1)
print(num2)

# La funcion float() y int() permiten cambiar el tipo de dato por uno de estos dos
print(float(num1)) # Convertimos el tipo de dato entero a float
print(type(float(num1)))

print(int(num2)) # Cambiamos el tipo de dato float a entero
print(type(int(num2)))
