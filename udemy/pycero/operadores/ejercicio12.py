# Operadores en Python
# Probando las operaciones aritmeticas en Python
# Solicitamos los datos al usuario
print("Deme un valor 1:")
a = int(input()) # Concatenamos de inmediato y solicitamos sobre la misma los datos al usuario
print("Deme un valor 2:")
b = int(input())

# Ejemplo Suma
suma = a + b
# Ejemplo Resta
resta = a - b
# Ejemplo Multiplicacion
multiplicacion = a * b
# Ejemplo Division
division = a / b
# Ejemplo Modulo, que es el residuo de la division
modulo = a % b
# Ejemplo exponenciacion
exponente = a ** b
# Ejemplo floor -> Que es la division entera
floor = a // b 

# Ahora mostramos por consola
print(suma)
print(resta)
print(multiplicacion)
print(division)
print(modulo)
print(exponente)
print(floor)