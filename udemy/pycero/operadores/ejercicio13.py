# Operadores de Comparacion

# Comparacion igual
x = 3
y = 5 # Aqui asignamos valores a las variables
print( x == y) # Aqui consultamos si x es igual a y
# Aqui es False

# Comparacion diferente (no igual)
print(x!= y) # x es distinto de y, dara True

# Mayor y menor
print(x > y) # X es mayor que y, sera False
print(x < y) # X es menor que y, sera True

# Mayor o igual -> Menor o igual
print(x >= y) # X es mayor o igual que -> False
print(x <= y) # x es menor o igual que -> True