# Operadores Logicos

# Variables de tipo Boleano(Verdad o Falso)
a = True
b = False

# Operadores

# and -> & -> Sera verdadero cuando ambos sean verdaderos
print(a and b) # a y b son verdaderos? en este caso son falsos
# Otra manera de usar el and es con &
print( a & b)

# or | -> Sera verdadero solo si los dos son falsos
print(a or b) # En este caso sera verdadero
print(a | b) # True

# not -> Negacion
print(not a) # En este caso a es True por lo cual el not lo dejara False
