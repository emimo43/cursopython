# Convirtiendo texto a Mayusculas

frase = "Hola Develoteca"

# Convertir texto a Mayuscula
print(frase)
print(frase.upper()) # El metodo upper() convertira el texto a Mayuscula

# Ahora creamos otra variable llamada FRASEMAYUSCULA que contendra a frase la cual contendra el metodo upper()
FRASEMAYUSCULA = frase.upper()
print(FRASEMAYUSCULA)