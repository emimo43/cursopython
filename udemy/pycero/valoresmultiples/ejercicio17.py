# Declaracion de Multiples variables

""" Asi en general usamos las variables:
x = 4
y = 3
z = 2
"""

# La definicion de multiples variables es asi:
x,y,z = 4,3,2 # El orden va tal cual

# Ahora mostraremos por consola el resultado
print(x)
print(y)
print(z)

# Tambien podemos usar string en la misma forma
a,b,c = "Rojo", "Azul", "Morado"
print(a)
print(b)
print(c)