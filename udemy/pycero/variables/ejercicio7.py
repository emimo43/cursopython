# Capitulo de Casting de variables, aqui convertiremos los datos a otro tipo de datos

# Declaramos las variables
x = str(5) # Aqui convertimos a string la variable x
y = int(3) # Aqui convertimos a int la variable y
z = float(4) # Aqui convertimos a float la variable z

# Ahora mostramos por consola los resultados
print(x)
print(y)
print(z)

# Ahora mostramos que tipo de datos contiene cada variable
print(type(x))
print(type(y))
print(type(z))