'''Operadores'''
# Declaramos las variables
x = 10
y = 5

# Suma
print(f"Este es el resultado de la suma de x + y: {x + y}")

# Resta
print(f"Este es el resultado de la resta de x - y: {x - y}")

# Multiplicacion
print(f"Este es el resultado de la multiplicacion de x * y: {x * y}")

# Division
print(f"Este es el resultado de la division de x / y: {x / y}")

# Division Entera
print(f"Este es el resultado de la division entera de x // y: {x // y}")

# Exponente
print(f"Este es el resultado de la potencia (exponente) de x ** y: {x ** y}")

# Operadores Comparativos

# Mayor que >
print(f"x es mayor que y: {x > y}")

# Menor que <
print(f"x es menor que y: {x < y}")

# Igual a ==
print(f"x es igual que y: {x == y}")

# No igual a !=
print(f"x es distinto que y: {x != y}")

# Mayor o igual >=
print(f"x es mayor o igual que y: {x >= y}")

# Menor o igual que <=
print(f"x es menor o igual que y: {x <= y}")