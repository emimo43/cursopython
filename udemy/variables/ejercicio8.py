# Nombre de variables: Existen reglas para los nombres de variables

# Variables con 1 solo caracter
x = 5
y = 3

# Ahora mostramos los resultados por consola
print(x)
print(y)
print(type(x))
print(type(y))

# Variables con palabras en minuscula y mayusculas
valora = 5
valorb = 3

print(valora)
print(valorb)
print(type(valora))
print(type(valorb))

# Variables usando los guiones bajos
valor_a = 5
_valor_a = 5

print(valor_a)
print(_valor_a)
print(type(valor_a))
print(type(_valor_a))

