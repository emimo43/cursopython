print("Bienvenido a la aplicacion:",nombre) # Esta es una forma de imprimir la variable(concatenacion)
print(f"Bienvenido a la aplicacion:{nombre}") # Esta es la mejor forma de concatenacion
print(f"Bienvenido a tu aplicacion {nombre}")