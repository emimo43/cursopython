# Ejercicio de declaracion de variables
"""Crear tres variables las cuales contengan el nombre, numero de telefono y correo electronico, luego mostrar por pantalla el resultado"""

# Declaramos las varibles
nombre = "Enrique Antonio Lueiza Mimó" # Dato str
telefono = 966137591 # dato int
email = 'e.mimo.lueiza@gmail.com' # Dato str

# Ahora mostramos por consola el resultado y luego ver que tipo de dato son
print(nombre)
print(telefono)
print(email)

print(type(nombre))
print(type(telefono))
print(type(email))

# La funcion type() nos permite mostrar de que tipo de dato es la variable que estamos usando