# Aqui veremos los operadores de asignacion

miVariable = 10
print(miVariable)

# Re Asignacion
miVariable = miVariable + 1
print(miVariable)

# Una forma mas resumida
# Incremento con reasignacion
miVariable += 1
print(miVariable)

miVariable -= 2 # miVariable = miVariable - 2
print(miVariable)

miVariable *= 3 # miVariable = miVariable * 3
print(miVariable)

miVariable /= 2 # miVariable = miVariable / 2
print(miVariable)