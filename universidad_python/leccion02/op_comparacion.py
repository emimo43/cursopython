# En esta clase veremos los operadores de comparacion en Python

a = 4
b = 2

resultado = a == b # A es igual a b
print(f'El resultado == : {resultado}')

resultado = a != b
print(f'\nResultado != : {resultado}') # a es distinto de b

resultado = a > b # a es mayor que b
print(f'\nResultado > : {resultado}')

resultado = a >= b # a es mayor que b
print(f'\nResultado >= : {resultado}')

resultado = a < b # a es mayor que b
print(f'\nResultado < : {resultado}')

resultado = a <= b # a es mayor que b
print(f'\nResultado <= : {resultado}')