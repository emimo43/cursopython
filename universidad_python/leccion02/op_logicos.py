'''
    En esta clase veremos los operadores logicos en Python
'''
a = True
b = False

resultado = a and b # Las dos variables deben ser verdaderos para que el print de True
print(resultado)

resultado = a or b # Con una variable que sea verdadero nos da True
print(resultado)

# Negacion --> not
resultado = not a # En este caso nos debe dar el resultad de True -> Es un operador unario, solo necesita un valor
print(resultado)