resta = operandoA - OperandoB
print(f'\nResultado de la resta: {resta}')

multiplicacion = operandoA * OperandoB
print(f'\nResultado de la multiplicacion es: {multiplicacion}')

division = operandoA / OperandoB
print(f'\nEl resultado de la division es: {division}')

divisionEntera = operandoA // OperandoB
print(f'\nEl resultado de la division Entera es: {divisionEntera}')

modulo = operandoA % OperandoB
print(f'\nEl resultado del modulo es: {modulo}')

exponente = operandoA ** OperandoB
print(f'\nEl resultado del exponente es: {exponente}')