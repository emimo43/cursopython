# En esta clase veremos el Casting de variables, que es la conversion de tipo de datos

x = str(5) # Estamos convirtiendo el numero 5 a un str
y = int(3) # Estamos convirtiendo el numero 3 a un int
z = float(4) # Convertimos el 4 a un tipo float que es un decimal

print(x)
print(y)
print(z)

# Ahora lo vamos a corroborar con la funcion type
print(type(x))
print(type(y))
print(type(z))