# En esta clase veremos los operadores aritmeticos en Python

# suma
suma = 5 + 3
print(type(suma))
print(suma)

# resta
resta = 5 -3
print(type(resta))
print(resta)

# multiplicacion
multiplicacion = 5 * 3
print(type(multiplicacion))
print(multiplicacion)

# division
division =  3 / 5
print(type(division))
print(division)

# division entera 
divisionEntera = 3 // 5
print(type(divisionEntera))
print(divisionEntera)

# modulo
modulo = 10 % 2
print(type(modulo))
print(modulo)

# potencia
potencia = 5 ** 2
print(type(potencia))
print(potencia)

