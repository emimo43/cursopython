# En esta clase veremos los operadores de comparacion en Python

# comparacion
comparacion = 5 == 5
print(type(comparacion))
print(comparacion)

# diferente
diferente = 5 != 5
print(type(diferente))
print(diferente)

# mayor que
mayorQue = 5 > 5
print(type(mayorQue))
print(mayorQue)

# menor que
menorQue = 5 < 5
print(type(menorQue))
print(menorQue)

# mayor o igual que
mayorIgualQue = 5 >= 5
print(type(mayorIgualQue))
print(mayorIgualQue)

# menor o igual que
menorIgualQue = 5 <= 5
print(type(menorIgualQue))
print(menorIgualQue)