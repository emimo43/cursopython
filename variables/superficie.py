# Hallar la superficie de un cuadrado conociendo el valor de un lado
# La formula para calcular la superficie es superficie = lado * lado

# Solicitamos los datos al usuario
lado = int(input("Favor ingrese el valor del lado: "))
# Realizamos el calculo de la formula en la variable superficie
superficie = lado * lado
# Ahora mostramos por consola el resultado
print(f"El valor de la superficie es: {superficie}")