# En esta clase veremos las variables en Python

# Declaracion e inicializacion de las variables

numero = 10 # Aqui declaramos e inicializamos la variable con un valor de 10

# En Python no se puede declarar y no inicializar las variables

numero = 2
numero2 = 100

# Ahora mostramos por consola las variables
print(numero)
print(numero2)

# Ahora mostraremos por consola las dos variables en una sola linea
print(numero,numero2)

# Reasignacion de variables
numero = 20
print(numero)

# Ahora reasignamos otro valor a la variable
numero = 550
print(numero)

'''
    Las partes de una variable
    numero -> identificador
    
    = -> operador de asignacion
    
    20 -> valor de la variable
'''